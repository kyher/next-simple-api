import { Book } from "@prisma/client";
import type { NextPage } from "next";
import Head from "next/head";
import Link from "next/link";
import { useEffect, useState } from "react";
import { useSession, signIn } from "next-auth/react";
import { Card } from "../components/Card";
import styles from "../styles/Home.module.css";
import buttonStyles from "../styles/Button.module.css";
import { SignedIn } from "../components/SignedIn";
import { SignedOut } from "../components/SignedOut";

const Home: NextPage = () => {
  const { data: session } = useSession();
  const [books, setBooks] = useState<Book[]>();
  const [isLoading, setIsLoading] = useState(false);

  useEffect(() => {
    setIsLoading(true);
    fetch("api/books")
      .then((res) => res.json())
      .then((books) => {
        setBooks(books);
        setIsLoading(false);
      });
  }, []);

  if (session) {
    if (isLoading) return <p>Loading...</p>;
    if (!books) return <p>No book data</p>;
    return (
      <div className={styles.container}>
        <Head>
          <title>Create Next App</title>
          <meta name="description" content="Generated by create next app" />
          <link rel="icon" href="/favicon.ico" />
        </Head>

        <main className={styles.main}>
          {session.user && session.user.email ? (
            <SignedIn email={session.user.email} />
          ) : (
            ""
          )}
          <h1 className={styles.title}>Book List</h1>
          <div className={styles.bookList}>
            {books.map(({ id, title, author }) => (
              <Card key={id} id={id} title={title} author={author} />
            ))}
            <Link href="/book/create">
              <button className={buttonStyles.button}>Create new book</button>
            </Link>
          </div>
        </main>
      </div>
    );
  }
  return (
    <main className={styles.main}>
      <SignedOut />
    </main>
  );
};

export default Home;
